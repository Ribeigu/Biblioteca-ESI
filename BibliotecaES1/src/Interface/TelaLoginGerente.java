    private void BotaoLoginGerenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoLoginGerenteActionPerformed
        String texto = TxtLoginGerenteEmail.getText();
        String senha = String.valueOf(TxtLoginGerenteSenha.getPassword());
        ControladorBiblioteca biblioteca = new ControladorBiblioteca(); //ajeitar isso aqui
        if(biblioteca.FazerLoginFuncionario(texto, senha) && biblioteca.VerificarSeEGerente(texto)){
            new TelaGerente().setVisible(true);
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Acesso negado");
        }
    }//GEN-LAST:event_BotaoLoginGerenteActionPerformed

